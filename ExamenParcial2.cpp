#include <iostream>

using namespace std;

int main()
{
    int i, j, k, l, m, n, o, p;
    for (i = 0; i <= 1; i++) {
        for (j = 1; j <= 1; j++) {
            cout << i << " " << j << endl;
        }
    }
    for (k = 2; k <= 3; k++) {
        for (l = 2; l <= 2; l++) {
            cout << k << " " << l << endl;
        }
    }
    for (m = 4; m <= 5; m++) {
        for (n = 3; n <= 3; n++) {
            cout << m << " " << n << endl;
        }
    }
    for (o = 6; o <= 7; o++) {
        for (p = 4; p <= 4; p++) {
            cout << o << " " << p << endl;
        }
    }
    system("pause");
    return 0;
}
    
  

